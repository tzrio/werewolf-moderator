export type NarrationCategory = keyof typeof NARRATION;

export function getRandomNarration(category: NarrationCategory, replacements?: Record<string, string>): string {
  const lines = NARRATION[category];
  let line = lines[Math.floor(Math.random() * lines.length)];
  if (replacements) {
    for (const [key, value] of Object.entries(replacements)) {
      line = line.replaceAll(`{${key}}`, value);
    }
  }
  return line;
}
